        The history:<br />
        With a development codename of 'a song to keep us warm'... 58hours was dreamt up in 2002... 6 Los Angeles apartments, and one Los Angeles to San Francisco move later (we're now currently based in San Francisco) and you're looking at the result...<br />
        <br/>
        I've used nearly all of that time to compile the heaps of data from personal knowledge and from across the different parts of the web.. compiling, arranging, bending and twisting the data, all with the goal of bringing my revelations to you here on this site.
        Massive thanks... a big heaping spoonful of appreciation... to  <a href="http://www.greenplastic.com" target="58hours">greenplastic.com</a>, <a href="http://www.followmearound.com" target="58hours">followmearound.com</a>, Adriaan at <a href="http://www.ateaseweb.com" target="58hours">ateaseweb.com</a>,
        and <a href="http://www.nepasavaler.net" target="58hours">nepasavaler.net</a> as
        well as countless other 'heads for their diligence in keeping records,
        so that I might access them later... Thanks.</font>      
        <p class="style1"><span class="style5">Accuracy and selective record-keeping:<br />
          It seems to be a constant theme, so I might as well address it... <br />
          <ul>
          <li>#1)<br />
          (Q:) &quot;I've noticed a lot of innacurate setlists/missing setlists, what's up?&quot;
          <br />(A:) Thanks for noticing! (and I mean that in the best possible way.) The majority of our setlists rely on the accuracy, feedback and submissions of you the user.  With some 600 shows catalogued and with my full time (non-Radiohead related) employment, while I'd like to spend all day sleuthing across the web to determine the legitimacy of setlists which were entered during the fledgling days of 58hours, honestly I need help to make this list better.  I welcome all comments, and anytime someone mails me about a disputed setlist, it brings it up on my radar and I will investigate.  58hours does its best to ensure the accuracy of all of its setlists. <br />I enjoy viewing accurate data just as much as the next person, (my perverse excitement at seeing this data compiled is really one of the primary reasons why 58hours exists) and when I find obvious errors within our system, I work quickly to correct them. However, sometimes situations arise wherein  different accounts of the same show surface (this often happens with 'earlier' shows). Rather than removing the record in question, I normally try to investigate the rumoured setlist difference, and if I can determine the proposed setlist to be more accurate, the change will be made in the 58hours system.<br />
          Long story short.  Rather than complain, help us make it better. We're on the same side. :)<br />
          </li>
          
          <li>#2)<br />(Q:) &quot;Why don't you add radio/tv performances to the mix?&quot;<br />
          (A:) While we do have a few BBC performances in the system, I generally consider this database to be a collection of performances which fans were able to attend... If you're going to be attending a Radiohead show and would like to find the probability of your favourite song being played, radio &amp; television performances aren't exactly going to do you much good. (e.g. &quot;Life in a Glass House&quot; has been played for broadcast, but alas, never made it into a concert setlist) Ideally, I'd love to expand the system to include these sorts of tracks, but for now, it's still off on the horizon.</li>
          </ul></span><br />
