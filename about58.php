                      [THE NEW WAY] (premiered June 24, 2006 - at the Berkeley
                          2 show)<br />
                      <br />
                      Updates are made through the Flash-Lite 2 compatible handset... submitted directly to the 58hours system, then compared with other agents in the field, and after the validity of the live track has been confirmed (by live participant consensus) the track is then assumed to be valid and added to the live database build.*<br /><br />
                      Brian currently runs with a Flash-Lite 2 loaded Cingular
                      Nokia 6682 whilst still keeping a 1st-gen (non-3G) iPhone
                      in his back pocket... in case things get rough.<br />
                      </p>
                      <p>
                      [THE OLD WAY]<br /><br />
                      Through the modern marvels of technology, the live setlist update process is incredibly pain-free. Y'see we've got volunteer operatives at select Radiohead shows with web-enabled phones in-hand (With this method, Brian's reluctant choice was the Sprint Samsung A-500)<br />And from a simple web-enabled phone, the staffer is able to plug in directly to the database which powers the whole of 58hours. It's a one-man job. Thus, when the 58hours staffer plugs a new setlist track in, it goes DIRECTLY to you users. No waiting for the &quot;my friend told me that...&quot; stuff. This is real-time. We get it when you get it.<br /><br>
                            ABOUT THE UPLINK CHECKS:<br>
                            Prior to the live setlist build, the 58hours staff attending the show perform two quick "uplink checks". Uplink check #1 is performed when the staffer arrives at their seat/location inside of the venue. Uplink check #2 is then performed once the opening act has finished their set.  The next action you see on the site after uplink check #2 will be the live setlist.
                            <br />
                            <br />
                        TECH SPECS:<br />
                        The old system was successfully executed using
                        a web-enabled cell phone, a sprinkling of PHP, and the
                        wonders of mySQL hanging out on the back end.<br />
                        58hoursLive (the new system) uses the same as before,
                        but adds a dash of Flash-specific PHP code (on
                        the back-end) for good measure. <br />
